	if ((i == j) && (j == k)) {
		price = (1000 * i) + 10000;
		printf("%ld", price);
		return 0;
	}

	// 주사위 2개가 같은 경우
	if (((i == j)&&(j != k)) || ((i == k)&&(k != j))) {
		price = 1000 + (100 * i);
		printf("%ld", price);
		return 0;
	}
	else if (j == k) {
		price = 1000 + (100 * j);
		printf("%ld", price);
		return 0;
	}

	// 주사위가 다 다른 경우
	if ((i != j) && (i != k) && (j != k)) {
		// i가 제일 큰 경우
		if ((i > j) && (i > k)) {
			price = i * 100;
			printf("%ld", price);
			return 0;
		}

		// j가 제일 큰 경우
		if ((j > i) && (j > k)) { 
			price = j * 100;
			printf("%ld", price);
			return 0;
		}

		// k가 제일 큰 경우
		if ((k > i) && (k > j)) { 
			price = k * 100;
			printf("%ld", price);
			return 0;
		}
	}
